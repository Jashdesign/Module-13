my_string = "PythonProgramming"

print("Original string:", my_string)

print("Slice [0:6]:", my_string[0:6])

print("Slice [6:]:", my_string[6:])

print("Slice [1:5]:", my_string[1:5])

print("Slice [-5:]:", my_string[-5:])

print("Slice [::2]:", my_string[::2])

print("Slice[::-1]:", my_string[::-1])